<b>About DKTES</b>
<p>Inspite of the process of phenomenal growth of the textile industry which was taking place at Ichalkaranji during last 25 years there was no educational institute up to 1980 which could cater a industry's need of technically trained man power. The local industry was facing handicapped due to the dearth of technically qualified personnel. Different co-op. organizations from Ichalkaranji therefore came together under the leadership of Mr. K.B. Awade (Ex. M.P.) to educational society in 1981 with a view to start institutes and colleges to cater to education in the field of Textiles. Engineering. Management, Medicines and others so as to provide education facilities in various fields to students of this rural area of western region of the Maharashtra State and also to support the growth of Industry by providing technically qualified professionals. The education society thus formed was named after Late Shri. Dattajirav Kadam an ardent social worker and veteran co-operator of this town. In this endeavor, Ichalkaranji Municipality and a number of generous and philanthropic citizens extended their wholehearted support.</p>

<p>The Dattajirao Kadam Education society as its first step established the Textile and Engineering Institute in 1982. This institute was permitted by the State Government to start a Diploma course in textiles from the academic year 1982 to 83 without any grant-in aid from the Government. This was the first educational institute in the state to be started on grant basis and encouraged by the successful establishment of this, the Maharashatra State Government took a policy decision in 1983 to permit various social and industrial organizations of the state to start professional educational institutes, the decision which brought a revolution in the thinking and pattern of the education in the state.</p>

<p>Presently the institute conducts ten degree courses in Textiles, Mechanical Engineering, Computer Science & Engineering, Information Technology and Electronic Engineering, Electronics & Telecommunications Engineering, three diploma courses in Textiles and seven postgraduate course in textiles as well as Mechanical Engineering and Electronic Engineering The total student strength of the institute is around 3000 and has 176 teaching faculty members and 285 non-teaching staff. The management has provided excellent infrastructural facilities and a highly qualified staff. As a result of this, very high standard of education is maintained. Apart from regular teaching this institute is well known for its interaction with industry. In this field it is not only known in India but also has achieved recognition at International level with projects from Oman and Nigeria. The D.K.T.E. society has also extended facilities in other areas of education by starting Primary Schools, High schools, Junior college, Physical Education College and institute of Vocational Education, Management college, Commerce college, CBSE Curriculum school. Mr. K.B. Awade is the Chairman of this society, Mr. R.N. Kulkarni is the Vice-Chairman , Mr. R.V. Ketkar is the Treasurer and Mrs. S.U. Awade is the Hon. Secretary.</p>
